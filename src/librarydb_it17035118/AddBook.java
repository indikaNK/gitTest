    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try
        {
        String ISBN = jTextField1.getText();
        String Category = jTextField2.getText();
        String BookTitle = jTextField3.getText();
        String Author = jTextField4.getText();
        double price = Double.parseDouble(jTextField5.getText());
        int TotalPage = Integer.parseInt(jTextField6.getText());

        if (ISBN.length() == 0)
        {
        JOptionPane.showMessageDialog(null, "ISBN is Mandatory");
        }
        else
        {
        Book book = new Book(ISBN, BookTitle, Author, Category, price, TotalPage);
        DBObject doc = createDBObject(book);
        DB BookBD = null;
        try
        {
        BookBD = DBManager.getDatabase();
        }
        catch (UnknownHostException ex)
        {
        JOptionPane.showMessageDialog(null, "Error When Connecting to DB");
        }
        DBCollection col = BookBD.getCollection("Books");
        WriteResult result = col.insert(doc);
        JOptionPane.showMessageDialog(null, "Inserted Successfully");
                        jTextField1.setText(null);
                        jTextField2.setText(null);
                        jTextField3.setText(null);
                        jTextField4.setText(null);
                        jTextField5.setText(null);
                        jTextField6.setText(null);
        }
        } catch (Exception e)
        {
        JOptionPane.showMessageDialog(null, "Insertion Failed  please Fill Details to add!" + e.toString());
        jTextField1.grabFocus();
        } 
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String ISBN = jTextField1.getText();
          Book book = new Book(ISBN);
          DBObject doc = createRemoveDBObject(book);
          DB BookBD = null;
          try
          {
           BookBD = DBManager.getDatabase();
          }
          catch (UnknownHostException e)
          {
           JOptionPane.showMessageDialog(null, "Remove Failed " + e.toString());
          }
          DBCollection col = BookBD.getCollection("Books");
          WriteResult result = col.remove(doc);
           if(result.getN() > 0)
           {
           JOptionPane.showMessageDialog(null, Integer.toString(result.getN()) + "Record(s) Removed Sucessfully");
           }
           else
           {
           JOptionPane.showMessageDialog(null, "Could not find record to remove");
           }
    }//GEN-LAST:event_jButton1ActionPerformed

