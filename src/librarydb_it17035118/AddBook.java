    private static DBObject createDBObject(Book book_)
{
            BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
            docBuilder.append("ISBN", book_.ISBN);
            docBuilder.append("Category", book_.Category);
            docBuilder.append("BookTitle", book_.BookTitle);
            docBuilder.append("Author", book_.Author);
            docBuilder.append("price", book_.Price);
            docBuilder.append("TotalPage", book_.TotalPages);
             return docBuilder.get();
}
    
    
    
    private static DBObject createRemoveDBObject(Book book_)
{
            BasicDBObjectBuilder docBuilder = BasicDBObjectBuilder.start();
            docBuilder.append("ISBN", book_.ISBN);
            return docBuilder.get();
}

